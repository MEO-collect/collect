/**
 * カルテフォーマット定義
 * 各フォーマットはAIへのプロンプトと出力テンプレートを持つ
 */

export interface KarteFormat {
  id: string;
  name: string;
  category: string;
  description: string;
  systemPrompt: string;
  outputTemplate: (patientInfo?: PatientInfo) => string;
  mergePrompt: (partialSummaries: string[], finalTemplate: string) => string;
  chunkPrompt: (chunk: string, index: number, total: number, finalTemplate: string) => string;
}

export interface PatientInfo {
  patientId?: string;
  patientName?: string;
  age?: string;
  gender?: string;
}

